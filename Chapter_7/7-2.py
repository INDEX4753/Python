"""
7-2 os模块的使用  获取绝对路径、路径分离和连接
"""

import os

# 获取绝对路径
## 相对路径：'.'表示当前目录，'..'表示上一级目录
## 绝对路径：从根目录开始的完整路径
print(f"当前目录的绝对路径: {os.path.abspath('.')}")
## 输出当前目录的绝对路径
print(f"上一级目录的绝对路径: {os.path.abspath('..')}")
## 输出上一级目录的绝对路径
## 平时使用相对路径比较多，绝对路径在一些特殊情况下才会使用

# 路径分离和连接
## 路径分离：将路径分为目录和文件名两部分
print(f"路径分离示例: {os.path.split('/usr/local/bin/python3')}")
## 输出路径分离示例
## 路径连接：将目录和文件名合并为完整路径
print(f"路径连接示例: {os.path.join('/usr/local/bin', 'python3')}")
## 输出路径连接示例

