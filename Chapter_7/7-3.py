# 文件打开
## 语法
## file_object = open(file_name, access_mode)
## file_object：文件对象，用于后续的文件操作
## file_name：要打开的文件名（包括路径）
## access_mode：文件打开模式，如只读、写入、追加等
## 常用的 access_mode 有：
## 'r'：只读模式（默认）
## 'w'：写入模式，文件不存在时创建，存在时清空内容
## 'a'：追加模式，文件不存在时创建，存在时在末尾追加内容
## 'b'：二进制模式，用于处理非文本文件
## '+'：读写模式，可同时读写文件
## 'x'：独占创建模式，文件不存在时创建，存在时报错
## 't'：文本模式（默认），用于处理文本文件
## 常用的 access_mode 组合有：
## 'rt'：只读文本模式（默认）
## 'wt'：写入文本模式
## 'at'：追加文本模式
## 'rb'：只读二进制模式
## 'wb'：写入二进制模式
## 'ab'：追加二进制模式
## 文件指针
## 文件指针是一个用于记录当前文件读写位置的指针，它会随着文件的读写操作而移动。
## 每个文件对象都有一个文件指针，默认情况下，文件指针指向文件的开头。
## 可以使用文件对象的方法来操作文件指针，如 seek()、tell() 等。
## seek(offset, whence)：将文件指针移动到指定位置
## offset：偏移量，单位为字节
## whence：参考位置，0 表示文件开头，1 表示当前位置，2 表示文件末尾
## tell()：返回当前文件指针的位置，单位为字节

# 文件关闭
## 语法
## file_object.close()
## file_object：要关闭的文件对象
## 关闭文件后，文件对象将无法再用于读写操作。
## 建议在使用完文件后及时关闭，以释放系统资源。
## 可以使用 with 语句来自动关闭文件，无需手动调用 close() 方法。
## with open(file_name, access_mode) as file_object:
##     # 文件操作代码
## 文件操作代码执行完毕后，文件会自动关闭，无需手动调用 close() 方法。
## 建议在使用 with 语句时，只在需要进行文件操作的代码块中使用，以避免文件对象被意外关闭。
## file_object.closed：返回文件是否已关闭，True 表示已关闭，False 表示未关闭

# 文件读写
## 语法
## file_object.read([size])：从文件中读取指定数量的字节或字符，返回读取的内容
## size：可选参数，指定要读取的字节或字符数量，默认读取所有内容(size = -1)
## file_object.write(string)：将字符串写入文件，返回写入的字节数
## string：要写入的字符串内容
## 文件读写操作完成后，文件指针会移动到写入或读取的末尾。
## 可以使用文件对象的方法来操作文件，如 seek()、tell() 等。